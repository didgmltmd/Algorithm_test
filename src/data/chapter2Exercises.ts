const options = (labels: string[]) => labels.map((label, index) => ({ id: String(index + 1), label }));
const answerLabel = (id: string) => ["①", "②", "③", "④", "⑤"][Number(id) - 1] ?? id;

const mc = (
  number: number,
  conceptSummary: string,
  prompt: string,
  labels: string[],
  answer: string,
  explanation: string,
  pageNumber: number,
): ExerciseProblem => ({
  id: `c2-ex-${String(number).padStart(3, "0")}`,
  chapter: 2,
  chapterTitle: c2,
  title: `연습문제 ${number}`,
  kind: "multiple-choice",
  conceptSummary,
  prompt,
  pdfFile,
  pageNumber,
  options: options(labels),
  answers: [answer],
  answerText: `${answerLabel(answer)} ${labels[Number(answer) - 1]}`,
  hint: "선택지의 문장을 정의와 비교해 보세요.",
  explanation,
});

const multiSelect = (
  number: number,
  conceptSummary: string,
  prompt: string,
  labels: string[],
  answers: string[],
  explanation: string,
  pageNumber: number,
): ExerciseProblem => ({
  id: `c2-ex-${String(number).padStart(3, "0")}`,
  chapter: 2,
  chapterTitle: c2,
  title: `연습문제 ${number}`,
  kind: "multi-select",
  conceptSummary,
  prompt,
  pdfFile,
  pageNumber,
  options: options(labels),
  answers,
  answerText: answers.map((answer) => `${answerLabel(answer)} ${labels[Number(answer) - 1]}`).join("\n"),
  hint: "옳은 선택지가 여러 개일 수 있습니다. 각 선택지를 독립적으로 판단하세요.",
  explanation,
});

